import {Injectable} from '@angular/core';

export class Host {
  constructor(
    public id: number,
    public title: string,
    public price: number,
    public rating: number,
    public description: string,
    public categories: string[]) {
  }
}

export class Review {
  constructor(
    public id: number,
    public hostId: number,
    public timestamp: Date,
    public user: string,
    public rating: number,
    public comment: string) {
  }
}

@Injectable()
export class HostService {
  getHosts(): Host[] {
    return hosts.map(p => new Host(p.id, p.title, p.price, p.rating, p.description, p.categories));
  }

  getHostById(hostId: number): Host {
    return hosts.find(p => p.id === hostId);
  }

  getReviewsForHost(hostId: number): Review[] {
    return reviews
      .filter(r => r.hostId === hostId)
      .map(r => new Review(r.id, r.hostId, new Date(r.timestamp), r.user, r.rating, r.comment));
  }

  getAllCategories(): string[] {
    return ['north indian', 'Indian', 'south Indian'];
  }
}

